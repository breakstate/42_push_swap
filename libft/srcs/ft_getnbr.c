#include "libft.h"

int		ft_getnbr(char *str)
{
	int	ret;

	if (*str == '-' && str != NULL)
		return (-ft_getnbr(&str[1]));
	while ('0' <= *str && *str >= '9')
	{
		ret = (ret * 10) + (*str - '0');
		str++;
	}
	return (ret);
}
