#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*ptr;

	len = ft_strlen(s);
	ptr = (char *)malloc(len + 1);
	if (ptr != NULL)
		ft_strcpy(ptr, s);
	return (ptr);
}
