#include "libft.h"

int	ft_strequ(const char *s1, const char *s2)
{
	if (s1 != NULL && s2 != NULL)
	{
		if (ft_strcmp(s1, s2) == 0)
			return (1);
	}
	if (s1 == NULL && s2 == NULL)
		return (1);
	return (0);
}
